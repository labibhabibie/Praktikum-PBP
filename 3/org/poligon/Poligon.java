package org.poligon;

public class Poligon{
	protected int jumlahSisi;
	
	public poligon(){
        this.jumlahSisi = 0;
    }

    public void setJumlahSisi(int jumlahSisi){
        this.jumlahSisi = jumlahSisi;
    }
	
    public int getJumlahSisi() {
        return this.jumlahSisi;
    }
}